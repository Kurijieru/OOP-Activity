<h1>Tokyo</h1>
<p>Tokyo is the capital city of Japan. It is the most populous metropolitan area in the world, with over 37 million people living in the Greater Tokyo Area.</p>
<p>Originally a small fishing village named Edo, Tokyo became prominent in the early 17th century when it became the seat of the Tokugawa shogunate. It was renamed Tokyo, meaning "Eastern Capital," in 1868 when Emperor Meiji moved the capital from Kyoto.</p>
