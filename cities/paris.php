<h1>Paris</h1>
<p>Paris is the capital city of France. It is one of the most populous cities in the European Union, with a metropolitan area population of over 12 million people.</p>
<p>Located on the River Seine, Paris has been a significant settlement for over two millennia. Originally founded by a Celtic tribe known as the Parisii, it was later developed by the Romans as Lutetia before becoming the cultural and political heart of France.</p>
